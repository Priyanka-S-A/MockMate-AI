// We are using node v22, so native global fetch is available.

const API_URL = 'http://localhost:5000/api';
const testEmail = `testuser_${Date.now()}@example.com`;
const testPassword = 'Password123!';

async function runIntegrationTest() {
  try {
    console.log('--- Starting Integration Test ---');

    // 1. Register a new user
    console.log(`Registering user with email: ${testEmail}...`);
    const regRes = await fetch(`${API_URL}/auth/register`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        name: 'Gemini Test User',
        email: testEmail,
        password: testPassword
      })
    });

    const regData = await regRes.json();
    if (!regRes.ok) {
      throw new Error(`Registration failed: ${JSON.stringify(regData)}`);
    }
    const token = regData.token;
    console.log('User registered successfully. Token obtained.');

    // 2. Create an interview (this tests question generation via Gemini)
    console.log('\nCreating an interview (should trigger real Gemini question generation)...');
    const interviewRes = await fetch(`${API_URL}/interviews`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${token}`
      },
      body: JSON.stringify({
        domain: 'Node.js',
        difficulty: 'Intermediate',
        totalQuestions: 3
      })
    });

    const interviewData = await interviewRes.json();
    if (!interviewRes.ok) {
      throw new Error(`Interview creation failed: ${JSON.stringify(interviewData)}`);
    }

    console.log('Interview created successfully!');
    console.log('Interview ID:', interviewData._id);
    const firstQuestion = interviewData.currentQuestion;
    console.log('Generated Question Text:');
    console.log(`> "${firstQuestion.questionText}"`);

    if (!firstQuestion.questionText || firstQuestion.questionText.includes('Explain how the asynchronous') || firstQuestion.questionText.includes('What is the difference between setImmediate')) {
      // Mock questions would match MOCK_QUESTIONS['Node.js']
      console.log('Warning: The question text might be mock data. Double check if it came from Gemini.');
    } else {
      console.log('Success: The question text looks dynamic and generated by Gemini!');
    }

    // 3. Submit an answer (this tests answer evaluation via Gemini)
    console.log('\nSubmitting answer to the question (should trigger real Gemini answer evaluation)...');
    const answerRes = await fetch(`${API_URL}/interviews/${interviewData._id}/answer`, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${token}`
      },
      body: JSON.stringify({
        questionId: firstQuestion._id,
        userAnswer: 'Middleware functions in Express are functions that run during the request-response cycle. They have access to req, res, and the next() function to pass control to the next middleware.'
      })
    });

    const answerData = await answerRes.json();
    if (!answerRes.ok) {
      throw new Error(`Answer submission failed: ${JSON.stringify(answerData)}`);
    }

    console.log('Answer submitted successfully!');
    console.log('AI Evaluation Score:', answerData.evaluation.score);
    console.log('AI Technical Accuracy:', answerData.evaluation.technicalAccuracy);
    console.log('AI Completeness:', answerData.evaluation.completeness);
    console.log('AI Suggestions:', answerData.evaluation.suggestions);
    console.log('AI Ideal Answer:', answerData.evaluation.idealAnswer);

    console.log('\n--- Integration Test Completed Successfully ---');
  } catch (error) {
    console.error('Integration Test Failed:', error);
  }
}

runIntegrationTest();
